// ── Helpers ────────────────────────────────────────────────────────────────────
function statusColor(val) {
  if (val === 'PASS' || val === 'Pass' || val === 'Fixed' || val === '✅') return { bg: C.pass, text: C.passText };
  if (val === 'REGRESSION' || val === 'Worse') return { bg: C.reg, text: C.regText };
  if (val === 'FAIL' || val === 'Same' || val === '❌') return { bg: C.fail, text: C.failText };
  return { bg: C.lightGray, text: C.black };
}

function stripEmoji(str) {
  return str
    .replace(/✅/g, '[PASS]')
    .replace(/❌/g, '[FAIL]')
    .replace(/⚠️/g, '[WARN]')
    .replace(/🔴/g, '[HIGH]')
    .replace(/🟠/g, '[MED]')
    .replace(/🟡/g, '[LOW]')
    .replace(/🟢/g, '[OK]')
    .replace(/ℹ️/g, '[INFO]')
    .replace(/⏱/g,  '[TIME]')
    .replace(/[^\x00-\x7F]/g, '');
}

function cleanCell(str) {
  return stripEmoji(str || '').replace(/\*\*/g, '').replace(/`/g, '').trim();
}

