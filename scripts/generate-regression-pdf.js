// ── TABLE HELPER ───────────────────────────────────────────────────────────────
function drawTable(headers, rows, colWidths, { statusCols = [] } = {}) {
  const rowH    = 18;
  const padding = 4;
  const total   = colWidths.reduce((a, b) => a + b, 0);

  safeY(rowH * 2 + 10);

  // Header row
  let cx = ML;
  headers.forEach((h, i) => {
    doc.rect(cx, doc.y, colWidths[i], rowH).fill(C.headerBg);
    doc.fillColor(C.white).font('Helvetica-Bold').fontSize(8)
       .text(cleanCell(h), cx + padding, doc.y + 4, { width: colWidths[i] - padding * 2, lineBreak: false });
    cx += colWidths[i];
  });
  doc.moveDown(1.25);

  // Data rows
  rows.forEach((row, ri) => {
    safeY(rowH + 4);
    const rowBg = ri % 2 === 0 ? C.white : C.lightGray;
    doc.rect(ML, doc.y, total, rowH).fill(rowBg);

    cx = ML;
    row.forEach((cell, ci) => {
      const cellStr = cleanCell(String(cell));
      let bg   = null;
      let fg   = C.black;

      if (statusCols.includes(ci)) {
        const sc = statusColor(cellStr);
        bg = sc.bg; fg = sc.text;
        doc.rect(cx + 2, doc.y + 2, colWidths[ci] - 4, rowH - 4).fill(bg);
      }

      doc.fillColor(fg).font('Helvetica').fontSize(7.5)
         .text(cellStr, cx + padding, doc.y + 4, {
           width:    colWidths[ci] - padding * 2,
           lineBreak: false,
           ellipsis: true,
         });
      cx += colWidths[ci];
    });

    // row border
    doc.rect(ML, doc.y, total, rowH).strokeColor(C.border).lineWidth(0.3).stroke();
    doc.moveDown(1.25);
  });
  doc.moveDown(0.5);
}

